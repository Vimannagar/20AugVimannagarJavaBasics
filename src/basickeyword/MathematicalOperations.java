package basickeyword;

public class MathematicalOperations {
	
	public static void main(String[] args) {
		
		int a = 10;
		
		int b = 50;
//performing addition of 2 numbers		
		int c = a+b;
		
		System.out.println(c);
		
		System.out.println(2+2);//4
		
		
		
//					+			-				*				/
//	WAP to perform addition, substraction , multiplication and division of two numbers	
	
//		substraction
		int d = a-b;
		
		System.out.println(d);//-40
		
//		multiplication
		
		int e = a*b;
		
		System.out.println(e);//500
		
//		division
		double g = 10;
		double h = 50;
		double f = g/h;
		
		System.out.println(f);
		
		
	}

}
