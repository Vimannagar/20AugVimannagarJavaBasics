package basickeyword;

public class NonPrimitiveDataType {
	
	
	public static void main(String[] args) {
		
		String s = "Velocity";
		
		System.out.println(s);//Velocity
		
		System.out.println(s+" "+"corporate");// Velocity Corporate
		
		
		String s1 = "Training";
		
		System.out.println(s+" "+"corporate"+" "+s1);//Velocity Corporate Training
		
		System.out.println(2+2+2+"2");//62
		
		System.out.println(10+2+"5"+2+3);//12523
		
		int a = 50;
		
		String b = "abc";
		
		
		System.out.println(a+5+b);//55abc
		
	
		
	}

}
